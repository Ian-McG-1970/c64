BM_IRQ:   INC   $D019    ;VIC Interrupt Request Register (IRR)
          PHA
          TXA 
          PHA
          TYA 
          PHA 

          PLA 
          TAY
          PLA 
          TAX
          PLA
NMI_NOP:  RTI ; This is the irq handler for the NMI. Just returns without acknowledge. This prevents subsequent NMI's from interfering.

!ZONE SB_FILL
SB_FILL:  LDX   #31
.LOOP_H   LDY   #24
.LOOP_V       JSR   CHAR_SB_2
              DEY
              BPL   .LOOP_V
            DEX
            BPL   .LOOP_H
          RTS 
  

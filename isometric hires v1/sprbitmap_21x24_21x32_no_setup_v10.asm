          TYA
          ASL ; *2
          ASL ; *4
          ASL ; *8
          TAY
          LDA   SC_LO,Y
          STA   SCR 
          LDA   SC_HI,Y
         ora #128 
          STA   SCR+1 
          STX   REGX
          TXA
          ASL ; *2
          ASL ; *4
          ASL ; *8
          TAY
          LDA   REGY
          CLC
          ADC   REGX
          AND   #15
          TAX
          LDA   PATTERN,X
          LDX   #8
.LOOP:      STA   (SCR),Y
            INY
            DEX
            BNE   .LOOP
          LDX   REGX
          LDY   REGY
          RTS 

!ZONE MEMSET        
MEMSET       STY    .LSB_ONLY+1 ; store LSB count
             CPX    #0          ; MSB?     
             BEQ    .LSB_ONLY   ; no

             LDY    #0          ; yes so reset LSB
.MSB_LOOP  
.LSB_LOOP      STA    (SCR),Y   ; clear whole MSB
               DEY 
               BNE    .LSB_LOOP

              INC    SCR+1      ; inc MSB
              DEX               ; dec MSB count
              BNE    .MSB_LOOP

.LSB_ONLY    LDY    #0          ; LSB count 
             BEQ    .MS_END     ; not needed

.LAST_LSB_LOOP STA   (SCR),Y
               DEY 
               BNE   .LAST_LSB_LOOP
                
              STA   (SCR),Y     ; clear last Y (0)
 
.MS_END      RTS


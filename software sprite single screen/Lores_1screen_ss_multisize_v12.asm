		TAX

		LDA SPR_SPR_HGT_ADR_LO,X  ; get height address of number of lines to blit
		STA SPR_BLT_LOOP_RTN +1
		LDA SPR_SPR_HGT_ADR_HI,X
		STA SPR_BLT_LOOP_RTN +2

		LDA SPR_AND_SET_LO,X
		STA .SPR_AND_SET +1
		LDA SPR_AND_SET_HI,X
		STA .SPR_AND_SET +2

		LDA SPR_ORA_SET_LO,X
		STA .SPR_ORA_SET +1
		LDA SPR_ORA_SET_HI,X
		STA .SPR_ORA_SET +2

        LDA SPR_SPR_SCN_SET_LO,X
        STA .SPR_SCN_SET +1
        LDA SPR_SPR_SCN_SET_HI,X
        STA .SPR_SCN_SET +2
  
              LDA SPR_INC,X
              STA SPR_BLT_INC +1
  
              LDA SPR_SIZE_X2,X ; columns by rows
              STA SPR_CNT

				STX REGY
				LDY SPR_AND_HI,X
				LDA SPR_AND_LO,X
				TAX
.SPR_AND_SET  	JSR SPR_AND_SETUP

				LDX REGY
				LDY SPR_ORA_HI,X
				LDA SPR_ORA_LO,X
				TAX
.SPR_ORA_SET	JSR SPR_ORA_SETUP

              LDY REGX ; V
.SPR_SCN_SET  JSR SPR_SCN_SETUP

              LDX REGA ; H
              LDY SCR_TAB3,X
              LDX SPR_CNT ; 24 16 8 0 (needs to be colums*rows)
              CLC

              JMP (SPR_BLT_LOOP_RTN +1)

